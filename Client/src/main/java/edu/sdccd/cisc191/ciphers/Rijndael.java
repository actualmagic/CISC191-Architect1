            state[0][j] = (byte) (GALOIS_MULT14[tempCol[0] & 0xff] ^ GALOIS_MULT11[tempCol[1] & 0xff] ^ GALOIS_MULT13[tempCol[2] & 0xff] ^ GALOIS_MULT9[tempCol[3] & 0xff]);
